    static private function addCapabilites() {
        $role = get_role( 'administrator' );
        $role->add_cap( 'jsvehiclemanager' );
        $role->add_cap( 'jsvehiclemanager_vehicles' );
        $role2 = get_role( 'contributor' );
        $role2->add_cap( 'jsvehiclemanager_vehicles' );
    }

    static private function checkUpdates() {
