    static private function insertMenu() {
        $db = new jsvehiclemanagerdb();
        $query = "Select COUNT(id) FROM `#__posts` WHERE post_content LIKE '%[jsvehiclemanager_control_panel]%'";
        $db->setQuery($query);
        $pageexist = $db->loadResult();
        if ($pageexist == 0) {
            $post = array(
                'post_name' => 'js-vehicle-manager-controlpanel',
                'post_title' => 'Control Panel',
                'post_status' => 'publish',
                'post_content' => '[jsvehiclemanager_control_panel]',
                'post_type' => 'page'
            );
            wp_insert_post($post);
        } else {
            $query = "UPDATE `#__posts` SET post_status = 'publish' WHERE post_content LIKE '%[jsvehiclemanager_control_panel]%'";
            $db->setQuery($query);
            $db->query();
        }
        update_option('rewrite_rules', '');
    }

